// Language management functions
class LanguageManager {
    constructor() {
        this.currentLang = this.getStoredLanguage() || 'en';
        this.init();
    }

    // Get stored language from localStorage
    getStoredLanguage() {
        return localStorage.getItem('osc-language');
    }

    // Store language preference
    setStoredLanguage(lang) {
        localStorage.setItem('osc-language', lang);
        this.currentLang = lang;
    }

    // Switch language and update all content
    switchLanguage(lang) {
        this.setStoredLanguage(lang);
        this.updatePageContent(lang);
        this.updateDocumentLanguage(lang);
        this.updatePageTitle(lang);
    }

    // Update all content on the page
    updatePageContent(lang) {
        // Update elements with data attributes
        document.querySelectorAll('[data-' + lang + ']').forEach(element => {
            element.textContent = element.getAttribute('data-' + lang);
        });

        // Update page-specific content
        this.updatePageSpecificContent(lang);
    }

    // Update page-specific content based on current page
    updatePageSpecificContent(lang) {
        const path = window.location.pathname;
        const translations = siteTranslations[lang];

        if (!translations) return;

        // Update page title
        if (document.title) {
            document.title = translations.title || document.title;
        }

        // Update specific elements based on page
        if (path.includes('mission') || path.includes('/mission')) {
            this.updateMissionPage(lang);
        } else if (path.includes('contact') || path.includes('/contact')) {
            this.updateContactPage(lang);
        } else if (path.includes('committee') || path.includes('/committee')) {
            this.updateCommitteePage(lang);
        } else if (path.includes('board') || path.includes('/board')) {
            this.updateBoardPage(lang);
        } else if (path.includes('conf2024') || path.includes('/conf2024')) {
            this.updateConferencePage(lang);
        } else if (path.includes('auth') || path.includes('/auth')) {
            this.updateAuthPage(lang);
        }
    }

    // Update mission page content
    updateMissionPage(lang) {
        const translations = siteTranslations[lang];
        if (!translations) return;

        const titleEl = document.querySelector('.mission-title');
        const subtitleEl = document.querySelector('.hero-subheader');
        const backEl = document.querySelector('.back-text');

        if (titleEl) titleEl.textContent = translations.missionTitle;
        if (subtitleEl) subtitleEl.textContent = translations.missionSubtitle;
        if (backEl) backEl.textContent = translations.backToHome;
    }

    // Update contact page content
    updateContactPage(lang) {
        const translations = siteTranslations[lang];
        if (!translations) return;

        const titleEl = document.querySelector('.contact-title');
        const backEl = document.querySelector('.back-text');

        if (titleEl) titleEl.textContent = translations.contactTitle;
        if (backEl) backEl.textContent = translations.backToHome;
    }

    // Update committee page content
    updateCommitteePage(lang) {
        const translations = siteTranslations[lang];
        if (!translations) return;

        const titleEl = document.querySelector('.committee-title, .page-title');
        const backEl = document.querySelector('.back-text');

        if (titleEl) titleEl.textContent = translations.committeeTitle;
        if (backEl) backEl.textContent = translations.backToHome;
    }

    // Update board page content
    updateBoardPage(lang) {
        const translations = siteTranslations[lang];
        if (!translations) return;

        const titleEl = document.querySelector('.board-title, .page-title');
        const backEl = document.querySelector('.back-text');

        if (titleEl) titleEl.textContent = translations.boardTitle;
        if (backEl) backEl.textContent = translations.backToHome;
    }

    // Update conference page content
    updateConferencePage(lang) {
        const translations = siteTranslations[lang];
        if (!translations) return;

        const titleEl = document.querySelector('.conference-title, .page-title');
        const backEl = document.querySelector('.back-text');

        if (titleEl) titleEl.textContent = translations.conferenceTitle;
        if (backEl) backEl.textContent = translations.backToHome;
    }

    // Update auth page content
    updateAuthPage(lang) {
        const translations = siteTranslations[lang];
        if (!translations) return;

        const titleEl = document.querySelector('.auth-title, .page-title');
        const backEl = document.querySelector('.back-text');

        if (titleEl) titleEl.textContent = translations.authTitle;
        if (backEl) backEl.textContent = translations.backToHome;
    }

    // Update document language attribute
    updateDocumentLanguage(lang) {
        document.documentElement.lang = lang;
    }

    // Update page title
    updatePageTitle(lang) {
        const translations = siteTranslations[lang];
        if (translations && translations.title) {
            document.title = translations.title;
        }
    }

    // Initialize language manager
    init() {
        // Apply stored language on page load
        this.updatePageContent(this.currentLang);
        this.updateDocumentLanguage(this.currentLang);
        this.updatePageTitle(this.currentLang);
    }

    // Get current language
    getCurrentLanguage() {
        return this.currentLang;
    }
}

// Initialize language manager when DOM is loaded
document.addEventListener('DOMContentLoaded', function() {
    window.languageManager = new LanguageManager();
});

// Export for use in other scripts
if (typeof module !== 'undefined' && module.exports) {
    module.exports = { LanguageManager, siteTranslations };
} 